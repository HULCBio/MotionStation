% ISNODE　 ノードの検出
% R = ISNODE(T,N) は、ノード N がツリー構造 T の中に存在している場合は1を、そし
% て存在していない場合は0を出力します。
%
% N は、ノードのインデックスを含む列ベクトル、または、行列のいずれかです。この中
% には、ノードの深さと位置が含まれています。行列の場合、N(i,1) は i 番目のノード
% の深さ、N(i,2) は i 番目のノードの位置をそれぞれ示しています。
%
% ノードは、左から右、上から下へと番号付けられています。ルートとなるインデックス
% は0です。
%

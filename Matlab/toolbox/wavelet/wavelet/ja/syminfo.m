% Symlets Wavelets
%
% 一般的特性: 
% 与えられたサポート幅に対して、最小の非対称性と最大数の vanishing モーメントを
% 有するコンパクトサポートされたウェーブレットです。関連するスケーリングフィルタ
% は、ほぼ最小位相フィルタになります。
%			
% ファミリ                Symlets
% 略称                    sym
% 次数 N                  N = 2、3、...
% 例題                    sym2、sym8
%
% 直交性                  あり
% 双直交性                あり
% コンパクトサポート      あり
% 離散ウェーブレット変換  可
% 連続ウェーブレット変換  可
%
% サポート長              2N-1
% フィルタ長              2N
% レギュラリティ          
% 対称性                  ほぼ対称
% psiに関するvanishing 
% モーメントの数          N
%

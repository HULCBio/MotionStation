% WTREEMGR     ツリー構造に対する管理コマンド
% 
%   VARARGOUT = WTREEMGR(OPT,TREE,VARARGIN)
% 
% OPTに対しては、つぎに示す値を設定できます。それぞれの関連する使用法については、
% 参考欄にリストアップしたそれぞれの関数を参照してください。
%
% 'allnodes'  : ツリーノードの取り扱いのユーティリティ
% 'isnode'    : 存在するノードに対する評価
% 'istnode'   : 不連続ノードの検出
% 'create'    : ツリーの作成
% 'nodeasc'   : 設定したノードの上層のノードインデックスまたは深さ/位置を出力
% 'nodedesc'  : 設定したノードの下層のノードインデックスまたは深さ/位置を出力
% 'nodepar'   : 親ノードの出力
% 'ntnode'    : 不連続ノード数
% 'tnodes'    : 不連続ノードの検出
% 'leaves'    : 不連続ノードの出力
% 'noleaves'  : 不連続でないノードの検出
% 'order'     : ツリーの次数
% 'depth'     : ツリーの深さ
%
% ツリー構造の作成に関しては、MAKETREE を参照してください。
%
% 参考： ALLNODES, ISNODE, ISTNODE, LEAVES, MAKETREE, 
%        NODEASC, NODEDESC, NODEPAR, NOLEAVES, NTNODE, TNODES,
%        TREEDPTH, TREEORD.




% RTCLEAR   タイマおよび履歴変数を消去
%
% RTCLEARは、定義された全てのタイマと履歴変数を消去します。
% RTCLEAR ALLも同じです。
% RTCLEAR(T)は、タイマTを消去します。
% RTCLEAR(NAME)は、履歴変数NAMEを消去します。
% RTCLEAR(T1,NAME1,T2,T3,...)は、一度に複数のオブジェクトを消去します。
%
% この関数は、MATLABコマンドラインから直接利用されます。
% これは、緊急停止関数として用いることができます。


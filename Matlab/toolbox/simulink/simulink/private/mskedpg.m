  IconPageVisible = 'on';

  %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
  %% Since the following uicontrols appears on all 3 screens, it      %%
  %% should be placed on the screen that appears first. In this case, %%
  %% I've changed it from the Doc Page to the Icon Page.              %%
  %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

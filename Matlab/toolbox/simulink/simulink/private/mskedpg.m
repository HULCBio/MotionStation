function mskedpg(pagename, dlgHandle, BlockHandle, position)
%MSKEDPG creates the three different pages of the Mask Editor.
%   MSKEDPG creates the three different pages (Icon, Parameter, and
%   Documentation) for the Mask Editor.

%   Copyright 1990-2002 The MathWorks, Inc.
%   $Revision: 1.30 $
%   Sanjai Singh  05-01-96

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% Create the correct page %%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
switch (pagename)

  case 'IconPage'
    IconPage(dlgHandle, BlockHandle, position)

  case 'ParamPage'
    ParamPage(dlgHandle, BlockHandle, position)

  case 'DocPage'
    DocPage(dlgHandle, BlockHandle, position)

  otherwise
    error(['Unknown page of the Mask Editor : ' pagename]);

end  % ... switch (pagename)

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% End of main function : mskedpg %%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%  DocPage : Creates the Documentation Page of the Mask Editor  %
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
function  DocPage(dlgHandle, BlockHandle, position)
%
% DOCPAGE : Creates the Documentation Page of the Mask Editor
%           The Mask Type is always created by the first Page.
%           This page adds :
%           - Mask Description
%           - Mask Help
%
% Keep the handles of only the Edit Uicontrols because only they
% contain the needed information. Store in Figure's UserData.
%
  SyncData      = masksync('SyncDocPage', dlgHandle, BlockHandle);
  UIData        = masksync('UIGeom', dlgHandle, BlockHandle, position);
  Data          = get(dlgHandle, 'UserData');
  DocPageNumber = 'page3';

  %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
  % UIControls for the Description of the Masked Block. %
  %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
  uicontrol(...
      'Style',                  'text', ...
      'Tag',                    DocPageNumber, ...
      'horizontalalignment',    'left',...
      'Position',               [15 (UIData.Height- 70) 125 20 ],...
      'Visible',                'off',...
      'String',                 'Block description:');

  Data.DocPage.MaskDescriptionHandle = uicontrol(...
      'Style',                  'edit', ...
      'Tag',                    DocPageNumber,  ...
      'Min',                    0,...
      'Max',                    2,...
      'HorizontalAlignment',    'left',...
      'Visible',                'off',...
      'String',                 {SyncData.DescriptionString},...
      'Position',               [20 (UIData.Height-160) (UIData.Width-15) 90], ...
      'backgroundColor',        [1 1 1]);

  %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
  % UIControls for the Help of the Masked Block.        %
  %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
  uicontrol(...
      'Tag',                    DocPageNumber,...
      'Style',                  'text',...
      'HorizontalAlignment',    'left',...
      'Position',               [15 (UIData.Height-205) 200  20],...
      'Visible',                'off',...
      'String',                 'Block help:');

  Data.DocPage.MaskHelpHandle = uicontrol(...
      'Tag',                    DocPageNumber,...
      'Style',                  'edit',...
      'Min',                    0,...
      'Max',                    2,...
      'horizontalAlignment',    'left',...
      'Position',               [20  (UIData.Height-315) (UIData.Width-15) 110],...
      'Visible',                'off',...
      'String',                 {SyncData.HelpString},...
      'BackgroundColor',        [1 1 1]);

  %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
  %% Set the USERDATA of the Mask Editor %%
  %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
  set(dlgHandle, 'UserData', Data);

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% End of function : DocPage %%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


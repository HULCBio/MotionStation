% 同じ長さのベクトルでなければなりません。A と B の両方が列ベクトルの
% 場合、DOT(A,B) は A'*B と同じです。
%
% DOT(A,B) は、N次元配列 Aと B に対して、A と B の最初に1でない次元に
% 関してスカラ値を出力します。A と B は、同じサイズでなければなりません。
%
% DOT(A,B,DIM) は、A と B の次元 DIM のスカラ積を出力します。
%

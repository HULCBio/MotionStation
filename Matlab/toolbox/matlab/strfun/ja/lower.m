% 適用した結果を含んだ A と同じサイズのセル配列を出力します。
%
% サポートするキャラクタセット:
% 
%    PC     : Windows Latin-1
%    その他 : ISO Latin-1 (ISO 8859-1)
%           ：16ビットのキャラクタセットもサポートします。

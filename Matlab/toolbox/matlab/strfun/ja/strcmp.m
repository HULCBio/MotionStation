% STRCMP   文字列の比較
% 
% STRCMP(S1,S2) は、文字列 S1と S2 が等しい場合は1、他の場合0を出力します。
%
% STRCMP(S,T) は、S と T のいずれかが文字列のセル配列のとき、S や T と同じ
% サイズで、S と T の同じ要素に対して1を要素とし、そうでなければ0を要素と
% する配列を出力します。
% S と T は、同じサイズ(またはスカラのセル)でなければなりません。いずれかが、
% 正しい行数をもつキャラクタ配列でも構いません。
% 
% STRCMP は、国際キャラクタセットをサポートします。
%

% CLOSE   figureを閉じる
% 
% CLOSE(H) は、ハンドル番号がHであるウィンドウを閉じます。
% CLOSE自身では、カレントのfigureウィンドウを閉じます。
%
% CLOSE('name') は、指定したウィンドウを閉じます。
%
% CLOSE ALL は、開いているすべてのfigureウィンドウを閉じます。
% CLOSE ALL HIDDEN は、隠されているウィンドウも閉じます。
% 
% STATUS = CLOSE(...) は、指定したウィンドウが閉じていれば1、そうで
% なければ0を出力します。
%

% LIGHTING   ライティングモード
% 
% LIGHTING は、SURFACEとPATCHオブジェクトのライティングを制御します。
% SURFACEとPATCHオブジェクトは、関数 SURF、MESH、PCOLOR、FILL、FILL3 
% で作成されます。 
%
% LIGHTING FLAT は、カレントのグラフのライティングをflatに設定します。
% LIGHTING GOURAUD は、ライティングをgouraudに設定します。
% LIGHTING PHONG は、ライティングをphongに設定します。
% LIGHTING NONE は、ライティングを終了します。
% LIGHTING(AX,...) は、カレントの axes の代わりに AX を使います。
%

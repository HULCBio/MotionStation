% MAKESHOW   スライドショーのデモの作成
%
% このツールを使うことにより、ユーザ独自のグラフィックインタフェースを
% 作らなくても、対話的なスライドショーを作成することができます。
%
% このデモを行うためには、Command Windowにユーザ自身のMATLABコマンドをタ

% SUPERQUAD Barrの "superquadrics"楕円面
% 
% [x,y,z] = SUPERQUAD(n,e,m)は、nが垂直方向のroundness、eが水平方向の
% roundnessで、m個の面をもつ楕円面です。nとeの値が設定されない場合、ラン
% ダムな値が与えられます。mのデフォルト値は24です。
%
% SUPERQUAD(...)は、それ自身ではSURFプロットを行います。
%
% 参考文献:  A. H. Barr, IEEE Computer Graphics and Applications, 1981,
%           or, Graphics Gems III, David Kirk, editor, 1992.
%

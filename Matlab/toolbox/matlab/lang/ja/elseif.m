% ELSEIF   IFステートメントの条件
% 
% ELSEIF は、IF文と共に使います。ELSEIFに続くexpressionが真で、その前の
% IFとELSEIFのすべてのexpressionが偽の場合、ELSEIFの後のstatementが実行
% されます。実部がすべて非ゼロ要素の場合は、expressionは真とみなされます。
%
% ELSE IFはENDと対応しなければいけませんが、ELSEIFは対応する必要は
% ありません。
%
% IFステートメントの一般的な書式は、つぎのようになります。
%      IF expression
%        statements
%      ELSEIF expression
%        statements
%      ELSE
%        statements
%      END
%
% 参考：IF, ELSE, END.



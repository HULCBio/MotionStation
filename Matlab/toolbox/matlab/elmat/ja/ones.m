%ONES   すべての要素が1の配列の作成
% ONES(N)は、N行N列の要素がすべて1の行列を出力します。
%
% ONES(M,N) または ONES([M,N]) は、要素がすべて1のM行N列の行列を出力します。
%
% ONES(M,N,P,...) または ONES([M N P ...]) は、要素がすべて1の M×N×P×... 
% 配列です。
%
% ONES(SIZE(A)) Aと同じサイズですべての要素が1の配列です。
%
% 引数なしの ONES は、スカラー 1 です。
%
% ONES(M,N,...,CLASSNAME) または ONES([M,N,...],CLASSNAME) は、クラス 
% CLASSNAME の 1 からなる M×N×... 配列です。
%
% 例題:
%   x = ones(2,3,'int8');
%

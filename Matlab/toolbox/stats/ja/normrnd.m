% NORMRND   正規分布のランダム行列
%
% R = NORMRND(MU,SIGMA) は、平均 MU と標準偏差 SIGMA をもつ正規分布から
% 抽出された乱数の行列を出力します。R の大きさは、MU と SIGMA が共に
% 行列の場合、それらに共通する大きさになります。どちらかのパラメータが
% スカラの場合、R の大きさは、もう一方のパラメータと同じ大きさになります。
% また、R = NORMRND(MU.SIGMA,M,N) は、M行N列の行列を出力します。
%
% 参考 : NORMCDF, NORMFIT, NORMINV, NORMLIKE, NORMPDF, NORMSTAT, RANDN.



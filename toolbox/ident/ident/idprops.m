		 '                        at the position in question. See also SETPNAME.'...
         '                       Default: []'...
         ' '...
         ' Trace           - Determines information about the minimization'...
         '                   furnished to the screen.'...
         '                   Assignable values:'...
         '                      * ''Off'' (default) No info printed'...
         '                      * ''On'' Info about criterion values given'...
         '                      * ''Full'' Info about parameters and criterion'...
         '                          values given'...
         ' '...
         ' N4Weight         - Variables that determine algorithmic details'...
         ' N4Horizons         for the N4SID method. See Help N4SID.'...
         ' '...

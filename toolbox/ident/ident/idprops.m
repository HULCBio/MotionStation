%      'algorithm':  Properties associated with the algorithm.
%      'estimation': Properties associated with the estimation result
%      'focus':      Focus estimation fit to certain frequency ranges
%   
%   Note that you can type
%      idprops idpoly
%   as shorthand for
%      idprops('idpoly'), and also use just the first letters.
%
%   See also IDHELP, SET, GET, IDPOLY, IDARX, IDSS, IDGREY, IDDATA, IDFRD

%   Copyright 1986-2003 The MathWorks, Inc.
%   $Revision: 1.21.4.1 $  $Date: 2004/04/10 23:19:02 $



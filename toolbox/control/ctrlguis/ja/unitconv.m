% UNITCONV   データの単位変換
%
% V = UNITCONV(V,OldUnit,NewUnit) は、OldUnit から NewUnit の単位変換に
% 基づいて V の値を変換します。
%
% 振幅の単位タイプは以下のものがサポートされています:
%      'abs'     ('absolute')
%      'dB'      ('decibels')
%
% 位相の単位タイプは以下のものがサポートされています:
%      'deg'     ('degrees')
%      'rad'     ('radians')
%
% 周波数の単位タイプは以下のものがサポートされています:
%      'Hz'      ('hertz')
%      'rad/sec' ('rad/s')



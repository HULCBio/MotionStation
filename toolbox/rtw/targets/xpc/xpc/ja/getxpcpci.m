% 素をもつ構造体配列です。各要素は、フィールド名のセットで情報を組み合わせ
% ています。構造体は、表示されたリストと比べ、割り当てられたベースアドレス
% やベースクラス、サブクラス等の、より多くの情報を含んでいます。
% 
% GETXPCPCI('all') は、xPC Target ブロックライブラリでサポートされているデ
% バイスだけでなく、調べられたすべての PCI デバイスについての情報を表示し
% ます。これは、Graphics Controller や Neywork Cards や SCSI カードを含ん

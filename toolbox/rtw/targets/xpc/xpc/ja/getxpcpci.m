% されているデバイスのみ表示されます。情報は、PCI バス番号 、スロット番号
% 割り当てられた IRQ 番号、ベンダー名、デバイス(ボード)名、デバイスタイプ
% ベンダー PCI Id、デバイス PCI Id 自身を含んでいます。ホストとターゲット
% の通信リンクが機能している場合(xpctargetping は、GETXPCPCI が読み込まれ
% る前に、SUCCESS を出力)のみ、情報が取得できます。Target アプリケーション
% がロードされている、または、ローダがアクティの場合 のみ、GETSPCPCI は、
% 実行されます。後者の場合、固有の PCI デバイスに割り当てられたリソース情
% 報を取得するには、PCI デバイスがモデルビルドプロセスより前にドライバーブ
% ロックダイアログボックスへ提供されなければなりません。これは、PCIバス番
% 号、スロット番号、割り当てられた IRQ 番号を含んでいます。

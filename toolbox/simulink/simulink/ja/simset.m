%
% AbsTol - 絶対許容誤差値　[ 正のスカラ　{1e-6} ] このスカラ値は、状態ベクトル
%          のすべての要素に適用されます。
% AbsTol は、可変ステップソルバのみに適用され、デフォルトは1e-6です。
%
% Refine - 出力リファインファクタ　[ 正の整数　{1} ] このプロパティは、指定し
%          たファクタだけ出力点数を増加させ、より滑らかな出力を生成します。
% リファイン中にソルバはゼロクロッシングもチェックします。
% Refineは、可変ステップソルバのみに適用され、デフォルトは1です。
% Refine は、出力時間が指定されている場合は、無視されます。
%
% MaxStep - ?X?e?b?v?T?C?Y?I?a?A [ ?3?I?X?J?‰?@{auto} ] MaxStep ?I?A‰A?I?

%
% MinStep - ?X?e?b?v?T?C?Y?I‰o?A [ ?3?I?X?J?‰?@{auto} ] ?U?1/2?I [ ?3?I?X
%           ?J?‰, "n?‰?(r)?"] Minstep?I?A‰A?I?X?e?b?v?\???o?I?Y?E"K-p?3?e

% SIMSET プロパティ
%
% Solver - 時間進行方法   [ VariableStepDiscrete | ode45 | ode23 | ode113
%          | ode15s | ode23s | FixedStepDiscrete | ode5 | ode4 | ode3 | o
%          de2 | ode1 ] このプロパティは、時間を進めるためにどのソルバを使用するかを指定します。
%
% RelTol - 相対許容誤差値　[ 正のスカラ　{1e-3} ] このスカラ値は、状態ベクトル
%          のすべての要素に適用されます。
% 各積分ステップでの推定誤差は、以下を満足します。

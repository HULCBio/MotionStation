%
% InitialStep - 推奨初期ステップサイズ [ 正のスカラ　{auto} ] InitialStep
%               は、可変ステップソルバのみに適用されます。
% ソルバは、まずInitialStep のステップサイズを試みます。
% デフォルトでは、ソルバが自動的に初期ステップサイズを決定します。
%
% MaxOrder - ODE15Sの最高次数 [ 1 | 2 | 3 | 4 | {5} ] MaxOrder は、ODE15Sの
%            みに適用され、デフォルトは5です。
%
% FixedStep - 固定ステップサイズ [ 正のスカラ] FixedStep は、固定ステップソ
%             ルバのみに適用されます。
% 離散系成分が含まれている場合は、デフォルトは基本的サンプル時間です。 そうで
% ない場合、デフォルトはシミュレーション区間の50分の1です。
%
% ExtrapolationOrder - ODE14X?A?I?O'}?I???" [ 1 | 2 | 3 | {4} ]  ODE14X?A

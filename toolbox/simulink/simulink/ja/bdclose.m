% BDCLOSE   任意のまたはすべてのSimulinkシステムウィンドウを無条件に閉じる
%
% BDCLOSE は、現在のシステムウィンドウを無条件かつ確認なしに閉じます。
% 最後に保存した後で行われたシステムの変更は、すべて失われます。
%
% BDCLOSE('SYS') は、指定したシステムウィンドウを閉じます。
%
% BDCLOSE('all') は、すべてのシステムウィンドウを閉じます。
%
% 例題:
%
% bdclose('vdp')
%
% は、vdpシステムを閉じます。
%
% 参考 : CLOSE_SYSTEM, OPEN_SYSTEM, NEW_SYSTEM, SAVE_SYSTEM.



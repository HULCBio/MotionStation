% [A B; C] は、A の行数と B の行数が等しく、A の列数と B の列数の和が
% C の列数と等しい場合に使用できます。連結表現では、行列は [A B;[C D]] 
% のような連結によって作成されます。この方法を使ってかなり複雑な行列を
% 分かりやすく作成することができます。

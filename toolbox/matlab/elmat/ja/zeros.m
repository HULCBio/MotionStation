%ZEROS  零の配列
% ZEROS(N) は、N行N列の零の行列です。
%
% ZEROS(M,N) または ZEROS([M,N]) は、M行N列の零の行列です。
%
% ZEROS(M,N,P,...) または ZEROS([M N P ...]) は、M×N×P×... の零の
% 配列です。
%
% ZEROS(SIZE(A)) は、A と同じ大きさで、すべて零です。
%
% 引数なしの ZEROS は、スカラー 0 です。
%
% ZEROS(M,N,...,CLASSNAME) または ZEROS([M,N,...],CLASSNAME) は、
% クラス CLASSNAME の零の M×N×... 配列です。
%
% 例題:
%   x = zeros(2,3,'int8');
%

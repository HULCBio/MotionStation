%NaN    Not-a-Number(不定値)
% NaNは、Not-a-Number(不定値)に対するIEEE代数の表現です。
% NaNは、数学的に定義されない0.0/0.0やinf-infのような演算の結果として
% 得られます。
%
% NaN('double') は、引数なしの NaN と同じです。
%
% NaN('single') は、NaN の単精度表現です。
%
% NaN(N) は、NaN の N行N列の行列です。
%
% NaN(M,N) または NaN([M,N]) は、NaN の M行N列の行列です。
%
% NaN(M,N,P,...) または NaN([M,N,P,...]) は、NaN の M×N×P×... 配列です。
%
% NaN(...,CLASSNAME) は、CLASSNAME により指定されるクラスの NaN の配列です。
% CLASSNAME は、'single' または 'double' のいずれかでなければなりません。
%

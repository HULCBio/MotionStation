%INF 無限大       
% INF は、正のの無限大を示すIEEE数値表現を出力します。ゼロ割り(1.0/0.0)
% のような演算や、オーバーフロー(exp(1000))などにより無限大は作られます。
%
% INF('double') は、入力なしの INF と同じです。
%
% INF('single') は、INF の単精度表現です。
%
% INF(N) は、INF の N行N列の行列です。
%
% INF(M,N) または INF([M,N]) は、INF の M行N列の行列です。
%
% INF(M,N,P,...) または INF([M,N,P,...]) は、INF の M×N×P×... 配列です。
%
% INF(...,CLASSNAME) は、CLASSNAME により指定されるクラスの INF の配列です。
% CLASSNAME は、'single' または 'double' のいずれかでなければなりません。
%

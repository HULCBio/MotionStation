% HDF   MATLAB-HDFゲートウェイ関数
% 
% HDF は、 National Center for Supercomputing Applicationsで開発された
% HDFライブラリへのMEX-ファイルインタフェースです。
%
% ドキュメントは、NCSA HDF Home Pageのhttp://hdf.ncsa.uiuc.eduより得る
% ことができます。
%
% 現在では、HDFは、HDFインタフェースSD, V, VS, VF, VH, AN, DFR8, DF24, 
% H, HE, HL, HDのすべてまたは一部をサポートしています。さらに、
% MathWorks独自のMLインタフェースは、ゲートウェイ管理のための2,3の
% ユーティリティを含んでいます。
%
% 関数は、HDF-EOSインタフェースGD, PT, SWにも利用できます。これらの関数
% は、それぞれ、HDFGD, HDFPT, HDFSWで呼び出されます。 
%
% この関数を直接使うことはできません。代わりに、インタフェース独自のもの
% を使ってください。たとえば、SDインタフェースのHDF関数にアクセスするに
% は、HDFSD を使います。
%
% 参考：HDFTOOL, HDFINFO, HDFREAD, HDFAN, HDFDF24,
%       HDFDFR8, HDFH, HDFHD, HDFHE, HDFML, HDFSD, 
%       HDFV, HDFVF, HDFVH, HDFVS, HDFGD, HDFPT, HDFSW.



% WAVEINFO　 ウェーブレットに関する情報
% WAVEINFO は、Toolbox 内のすべてのウェーブレットに関する情報を提供しま
% す。
%
% WAVEINFO('wname') は、文字引数 'wname' により指定された略称をもつウェ
% ーブレットファミリに関する情報を表示します。
%
% 利用可能な略称は、以下のとおりです。
%   'haar'   : Haar ウェーブレット
%   'db'     : Daubechies ウェーブレット
%   'sym'    : symlets
%   'coif'   : coiflets
%   'bior'   : 双直交ウェーブレット
%   'rbio'   : 逆双直交ウェーブレット
%   'meyr'   : Meyer ウェーブレット
%   'dmey'   : 離散 Meyer ウェーブレット
%   'gaus'   : Gaussian ウェーブレット
%   'mexh'   : Mexican hat ウェーブレット
%   'morl'   : Morlet ウェーブレット
%   'cgau'   : 複素 Gaussian ウェーブレット
%   'cmor'   : 複素 Morlet ウェーブレット
%   'shan'   : 複素 Shannon ウェーブレット
%   'fbsp'   : 複素周波数 B-spline ウェーブレット
%
% または、ユーザ自身のウェーブレットファミリに対して、自分で定義した略称% も用いることができます(WAVEMNGR を参照)。
%
% WAVEINFO('wsys') は、ウェーブレットパケットに関する情報を表示します。
% 
% 参考： WAVEMNGR.




% COIFINFO 　Coifletsウェーブレットに関する情報
%
% Coiflets ウェーブレット
%
% 一般的特性: 設定したサポート幅に関連して、phi と psi 双方に対する vanishing モ
% ーメントの数が最大であるコンパクトサポートされたウェーブレットです。
%
%   ファミリ　 　　　　　                         Coiflets
%   略称       　　　　                        　 coif
%   次数 N     　　　　　                         N = 1、2、...、5
%   例題                                  　　　　coif2、coif4
%
%   直交性                   　　　　　　　　　　 あり
%   双直交性                 　　　　　　　　　　 あり
%   コンパクトサポート      　　　　　　　　　　　あり
%   離散ウェーブレット変換  　　　　　　　　　　　可
%   連続ウェーブレット変換  　　　　　　　　　　　可
%   サポート長              　　　　　　　　　　　6N-1
%   フィルタの長さ        　　　　　　　　　　　  6N
%   レギュラリティ
%   対称性                                        ほぼ、対称
%   psi に対するvanishing モーメントの数　        2N
%   phi に対するvanishing モーメントの数          2N-1
%
% 参考文献： I. Daubechies, 
%            Ten lectures on wavelets, 
%            CBMS, SIAM, 61, 1994, 258-261.




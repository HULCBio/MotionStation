% [t,x,y] = pdsimul(pds,'traj',tf,'ut',xi,options)
%
% 関数'TRAJ'によって設定されたパラメータ軌道に沿った(アフィン)パラメータ
% システムのシミュレーション。関数PDSIMULは、線形時変システムの時間応答
% を積分するために、ODE15sを呼び出します。
%
% 出力引数を設定しないと、PDSIMULは、出力軌道をプロットします。
%
% 入力:
%   PDS       アフィンパラメータ依存システム(PSYSを参照)。

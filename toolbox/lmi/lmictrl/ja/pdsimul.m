%   TF        積分の最終時間(initial=0)。
%   'UT'      入力関数名u=UT(t)。デフォルトは、ステップ入力です。
%   XI        システムの初期状態(デフォルト = 0)。
%   OPTIONS   ODE積分に対する制御パラメータ(ODESETを参照)。
%
%  出力:
%   T         積分時間点。
%   X         状態軌道(X(:,1) = 1番目の状態等)。
%   Y         出力軌道(Y(:,1) = 1番目の出力等)。

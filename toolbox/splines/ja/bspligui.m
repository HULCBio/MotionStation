% BSPLIGUI   B-スプラインの実験ツール (GUI)
%
% BSPLIGUI は、節点列 -1:.5:1を用いて、B-スプラインがどれくらい節点に
% 依存するかを実験するためのGUIを起動します。
% 詳細な情報については、Helpメニューをクリックしてください。
%
% BSPLIGUI(KNOTS) は、節点列を KNOTS として起動しますが、端の節点と
% して、-1 と 1 をもつようにスケールします。
%
% BSPLIGUI(ARG) は、ARG を 'close', 'exit', 'finish', あるいは、'quit' と
% して、Closeと記されたボタンをクリックするか、GUIのウィンドウを簡単に
% 閉じるのと同様にこのGUIを終了します。
%
